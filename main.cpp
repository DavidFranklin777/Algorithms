#include <iostream>
#include <BreadFirstSearch.h>
#include <DepthFirstSearch.h>

using namespace std;


int main()
{

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \brief BreadthFirstSearch
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    Node *root = createnode(10);
    root->left = createnode(5);
    root->right = createnode(15);
    root->left->left = createnode(4);
    root->left->right = createnode(6);
    root->right->left = createnode(14);
    root->right->right = createnode(16);
    cout << "Breadth First Traversal of Binary tree: ";
    cout << "Height: " << height(root) << endl;
    bfs(root);


    cout << "\nDFS inorder: " << endl;
    printInorder(root);
    cout << "\nDFS preorder: " << endl;
    printPreorder(root);

    cout << "\nDFS postorder: " << endl;
    printPostorder(root);

    root->destructRecursive(root);

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \brief end BFS
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
